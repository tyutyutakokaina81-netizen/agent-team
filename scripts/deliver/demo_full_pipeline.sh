#!/bin/bash
# デモ用：全工程を1コマンドで自動実行
# ・テスト案件を作成
# ・構成・本文を自動生成＆保存
# ・品質チェック
# ・納品パッケージ作成
# 使い方: ./scripts/deliver/demo_full_pipeline.sh

set -e

REPO_DIR="$(cd "$(dirname "$0")/../.." && pwd)"
cd "$REPO_DIR"
SCRIPT_DIR="$REPO_DIR/scripts/deliver"

DATE=$(date '+%Y-%m-%d')
FOLDER_NAME="${DATE}_テスト株式会社_サンプル記事執筆"
FOLDER_PATH="$REPO_DIR/deliveries/$FOLDER_NAME"

echo "============================================================"
echo "🎬 受注→納品パイプライン デモ実行"
echo "============================================================"
echo "案件: $FOLDER_NAME"
echo ""

# ========== STEP 1: 案件フォルダ作成 ==========
echo "▶ STEP 1: 受注セットアップ"

if [[ -d "$FOLDER_PATH" ]]; then
    echo "  既存フォルダを使用: $FOLDER_NAME"
else
    mkdir -p "$FOLDER_PATH/drafts" "$FOLDER_PATH/prompts" "$FOLDER_PATH/final"

    cat > "$FOLDER_PATH/meta.json" <<META_EOF
{
  "date": "$DATE",
  "client": "テスト株式会社",
  "job_title": "サンプル記事執筆",
  "job_type": "article",
  "price": 5000,
  "deadline": "2026-04-28",
  "details": {
    "keyword": "副業 始め方",
    "char_count": "3000",
    "persona": "30代会社員",
    "tone": "丁寧",
    "reference_urls": ""
  },
  "memo": "デモ実行",
  "status": "in_progress"
}
META_EOF

    cat > "$FOLDER_PATH/README.md" <<'README_EOF'
# サンプル記事執筆（デモ）

デモ用の自動実行。30代会社員向け「副業 始め方」3000字記事。
README_EOF

    echo "  ✅ フォルダ作成完了"
fi
echo ""

# ========== STEP 2: プロンプト生成 ==========
echo "▶ STEP 2: プロンプト生成"
python3 "$SCRIPT_DIR/generate.py" "$FOLDER_NAME" > /dev/null 2>&1 || true
[[ -f "$FOLDER_PATH/prompts/1_structure.md" ]] && echo "  ✅ 1_structure.md 生成済み"
[[ -f "$FOLDER_PATH/prompts/2_body.md" ]] && echo "  ✅ 2_body.md 生成済み"
echo ""

# ========== STEP 3: 構成を保存 ==========
echo "▶ STEP 3: 構成を drafts/ に保存"

cat > "$FOLDER_PATH/drafts/1_structure.md" <<'STRUCTURE_EOF'
# タイトル案
1. 【2026年版】副業の始め方完全ガイド｜30代会社員向け
2. 30代会社員のための副業の始め方｜月5万円を目指す5ステップ
3. 副業の始め方がわからない？失敗しない最初の一歩を解説

# メタディスクリプション
30代会社員が副業を始める方法を徹底解説。失敗しない5ステップと初期投資ゼロで始められる仕事、確定申告の準備まで、初心者が知っておくべき全情報をお届けします。

# 記事構成

## H2-1：副業を始める前に確認すべき3つのこと
  ### H3-1-1：会社の副業規定を確認する
  ### H3-1-2：使える時間を明確にする
  ### H3-1-3：月の目標収入を決める

## H2-2：30代会社員におすすめの副業5選
  ### H3-2-1：Webライティング
  ### H3-2-2：データ入力
  ### H3-2-3：SNS運用代行
  ### H3-2-4：スキルシェア
  ### H3-2-5：テンプレート販売

## H2-3：副業の始め方【5ステップ】
  ### H3-3-1：STEP1 プラットフォーム登録
  ### H3-3-2：STEP2 プロフィール整備
  ### H3-3-3：STEP3 最初の5件は評価を優先
  ### H3-3-4：STEP4 リピート案件を獲得
  ### H3-3-5：STEP5 単価を上げる交渉

## H2-4：副業で失敗しないための注意点
  ### H3-4-1：怪しい案件を見抜く5つのサイン
  ### H3-4-2：確定申告の準備
  ### H3-4-3：本業との両立のコツ

## H2-5：副業を始めて最初の3ヶ月の目標
  ### H3-5-1：1ヶ月目：月1〜3万円
  ### H3-5-2：2ヶ月目：月5万円
  ### H3-5-3：3ヶ月目：月10万円
STRUCTURE_EOF

echo "  ✅ 1_structure.md 保存完了"
echo ""

# ========== STEP 4: 本文を保存 ==========
echo "▶ STEP 4: 本文記事を drafts/ に保存"

cat > "$FOLDER_PATH/drafts/article.md" <<'ARTICLE_EOF'
# 【2026年版】副業の始め方完全ガイド｜30代会社員向け

「副業を始めたいけど、何から手をつければいいかわからない」
「本業に支障が出ないか不安」
「騙されるリスクも怖い」

30代会社員の多くが抱える悩みです。

結論からお伝えすると、副業の始め方は「5つのステップ」を踏めば、初期投資ゼロで月5万円を目指せます。本記事では、副業 始め方を迷っている30代会社員が、今日から実践できる具体的な手順を解説します。

## 副業を始める前に確認すべき3つのこと

副業 始め方で最初にやるべきは、準備の3点確認です。

### 会社の副業規定を確認する

まずは就業規則を確認しましょう。副業解禁の流れが進んでいますが、以下のような制限があるケースもあります。

- 競合他社での副業は禁止
- 事前申請が必要
- 週の労働時間に上限あり

トラブルを避けるため、必ず人事部に確認することをおすすめします。

### 使える時間を明確にする

次に、副業に使える時間を明確にしましょう。

| 時間帯 | 確保しやすい時間 |
|-------|---------------|
| 平日朝 | 1時間 |
| 平日夜 | 2時間 |
| 土日 | 4時間 |

週10〜15時間を目安に確保できれば、月5万円は現実的な目標です。

### 月の目標収入を決める

最後に、月の目標収入を明確にします。おすすめは「月5万円」。無理なく達成でき、家計への影響も大きい金額です。

ここがポイント：副業 始め方は準備8割。焦らず土台を固めましょう。

## 30代会社員におすすめの副業5選

副業 始め方で迷ったら、以下の5つから選んでください。いずれも初期投資ゼロで始められます。

### Webライティング

記事執筆の案件。1記事3,000〜5,000円、月10本で月5万円に到達します。AIを活用すれば執筆速度が2〜3倍になります。

### データ入力

Excelへの入力やコピペ作業。スキル不要で即始められます。時給は1,000〜1,400円が相場です。

### SNS運用代行

中小企業のInstagramやX運用を代行。1社契約で月5万円、2社で月10万円が目安です。

### スキルシェア

自分のスキルを出品型で販売します。寝てても売れる仕組みが作れます。

### テンプレート販売

Excel・Notionテンプレを販売。一度作れば永久に収益化可能です。

ここがポイント：副業 始め方の鉄則は「1つに絞って集中」。複数同時は失敗のもとです。

## 副業の始め方【5ステップ】

実際の副業 始め方を5ステップで解説します。

### STEP1 プラットフォーム登録

以下3つのプラットフォームに登録しましょう。

- クラウドワークス（案件数最多）
- ランサーズ（単価やや高め）
- ママワークス（主婦特化・継続案件多い）

登録は無料で、30分で完了します。

### STEP2 プロフィール整備

プロフィールの充実度で受注率が2〜3倍変わります。以下を必ず記入しましょう。

- 自己紹介（対応可能業務・得意分野）
- スキル10個以上
- 稼働時間（平日○時間、土日対応可否）
- プロフィール画像（イラストでもOK）

### STEP3 最初の5件は評価を優先

最初の5件は単価より評価を優先します。低単価案件でもタスク形式でもOK。評価5件貯めるまでは「練習期間」と割り切りましょう。

### STEP4 リピート案件を獲得

同じクライアントから2回目の依頼をもらえれば、時給は大きく上がります。納品時に「次回もぜひ」とアピールしましょう。

### STEP5 単価を上げる交渉

評価10件を超えたら、単価交渉を始めます。実績を根拠に、1.5倍〜2倍の単価を提示してみましょう。

ここがポイント：副業 始め方は「評価を買う→単価を上げる」の順番が鉄則です。

## 副業で失敗しないための注意点

副業 始め方で失敗する人には共通点があります。

### 怪しい案件を見抜く5つのサイン

以下のいずれかに該当する案件はスキップします。

1. 登録料や初期費用を要求される
2. LINEへの誘導がある
3. 月100万稼げるなど非現実的な収入を謳う
4. 作業内容が極めて曖昧
5. クライアントの評価が低い

### 確定申告の準備

副業収入が年20万円を超えると確定申告が必要です。以下を準備しましょう。

- 開業届の提出（税務署へ）
- 青色申告承認申請書（最大65万円控除）
- 経費の記録（領収書保管）

会計ソフトを使えば、確定申告は1日で完了します。

### 本業との両立のコツ

本業に支障を出さないために、以下を守りましょう。

- 睡眠時間は7時間確保
- 本業の時間中は副業作業禁止
- 週1日は完全休養日を作る

ここがポイント：副業は本業を邪魔しない範囲で続けるのが正解です。

## 副業を始めて最初の3ヶ月の目標

副業 始め方の成功パターンは、3ヶ月で形が見えてきます。

### 1ヶ月目：月1〜3万円

最初の1ヶ月は評価と実績作りの期間。月1〜3万円で十分です。

### 2ヶ月目：月5万円

評価10件達成後、単価アップ交渉で月5万円を目指します。

### 3ヶ月目：月10万円

継続案件を3〜5件確保し、月10万円到達が現実的な目標です。

ここがポイント：3ヶ月続けられれば、その後は自然と伸びていきます。

## まとめ：本日からできる3つの行動

副業 始め方を実践するために、本日中にできる3つの行動をお伝えします。

1. 就業規則を確認する（15分）
2. クラウドワークスに登録する（15分）
3. プロフィールを書く（30分）

所要時間は合計1時間です。

副業は知識ではなく行動の積み重ねです。本日の1時間が、3ヶ月後の月10万円につながります。

まずは本日、最初の一歩を踏み出しましょう。
ARTICLE_EOF

CHARS=$(wc -c < "$FOLDER_PATH/drafts/article.md" | tr -d ' ')
echo "  ✅ article.md 保存完了（${CHARS}バイト）"
echo ""

# ========== STEP 5: 品質チェック ==========
echo "▶ STEP 5: 品質チェック"
python3 "$SCRIPT_DIR/quality_check.py" "$FOLDER_NAME" 2>&1 | tail -40
echo ""

# ========== STEP 6: 納品パッケージ ==========
echo ""
echo "▶ STEP 6: 納品パッケージ生成"
python3 "$SCRIPT_DIR/package.py" "$FOLDER_NAME" 2>&1 | tail -20
echo ""

echo "============================================================"
echo "🎉 デモ実行完了"
echo "============================================================"
echo ""
echo "生成されたファイル："
echo "  📂 $FOLDER_PATH/"
echo "     ├── meta.json"
echo "     ├── README.md"
echo "     ├── prompts/          （Claudeプロンプト）"
