echo "     ├── final/            （納品ファイル）"
echo "     ├── delivery_email.txt（納品メール下書き）"
echo "     └── invoice_note.md   （請求メモ）"
echo ""
echo "🎯 確認コマンド："
echo "  ls -la $FOLDER_PATH/"
echo "  cat $FOLDER_PATH/delivery_email.txt"
