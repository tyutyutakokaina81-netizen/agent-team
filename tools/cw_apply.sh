#!/bin/bash
# cw_apply.sh — 応募文3件をテキストに書き出し、TextEditで一気に開く（macOS）。
#   ターミナルに一括で貼って実行 → 3つの応募文ウィンドウが開く → 各案件にコピペして応募。
#   ※「応募ボタンを押す」自体はCrowdWorks規約上、人間が手動で行う（自動応募は規約違反・停止リスク）。
set -e
DIR="受託/応募"
mkdir -p "$DIR"

cat > "$DIR/2026-06-24_01_AI活用記事.txt" <<'EOF'
【ご提案】
はじめまして。AI活用と働き方をテーマに、実体験ベースで記事を書いております。

私自身、日々の業務（情報発信・資料作成・問い合わせ対応の下書きなど）を実際にAIで回しており、「職場でAIをどう使うか」を“使っている側”の具体例として書けます。抽象論ではなく、
・どの作業を任せ、どこは人が確認するか
・つまずいた点とその回避法
まで、20代社会人がそのまま真似できる粒度で執筆します。

進め方は「AIで下書き→必ず自分で事実確認・推敲して仕上げる」なので、コピペ感のない自然な文章でお渡しします。1.4円・2,000文字で承知しております。下にサンプルを添付します。

──サンプル──
職場でAIをどう使う？20代社会人が実際にやっている活用法

「AIを仕事で使う」と聞くと身構えるかもしれませんが、実際にやっていることはとても地味です。派手な自動化ではなく、毎日の細かい作業を少しずつ軽くする——その積み重ねが、気づけば大きな時短になっています。

1. メールや問い合わせの「最初の一文」を書いてもらう
返信のたびに手が止まるのは、たいてい書き出しです。要件を箇条書きで渡し「丁寧で簡潔な返信の下書きを」と頼むと、数秒でたたき台ができます。私はそれを読み、自分の言葉に直して送ります。ゼロから書くより直す方がずっと速い。

2. 長い議事録やメモを要約する
貼り付けて「決定事項・宿題・次回までにやること」に分けてもらうと、自分用の整理メモになります。固有名詞や数字は最後に自分で確認します。

3. 資料の「たたき台」をつくる
「この内容で構成案を5項目」と頼み、骨組みだけ先に決めます。中身は自分で埋める。白紙とにらめっこする時間が減りました。

4. 調べ物の「入り口」にする
AIで当たりをつけ、必ず公式サイトや一次情報で裏を取ってから使います。鵜呑みにしない——これは外せません。

5. 自分の文章を見直してもらう
「意味は変えずに読みやすく」と頼み、納得した箇所だけ採用します。

たった一つのルール：AIに任せるのは下書きまで。送る・決めるのは必ず自分。社外秘や個人情報は入力しない。この線さえ守れば、AIは頼れる下書き役になります。
EOF

cat > "$DIR/2026-06-24_02_動物エピソード.txt" <<'EOF'
【ご提案】
はじめまして。暮らしや動物をテーマに、体験ベースのやわらかい記事を書いております。

「好きな動物の魅力や癒やされたエピソード」は、読者が思わず自分の体験を重ねたくなる“情景”を描けるかが肝だと考えています。あるある・情景・小さな発見を、誇張せず等身大の言葉でお届けします。AIで下書きを作り、必ず自分で推敲して自然な文章に仕上げるのでテンプレ感はありません。継続でのご協力も歓迎です。下にサンプルを添付します。

──サンプル──
猫がくれる、なんでもない時間のはなし

仕事で気持ちがささくれ立った日ほど、家に帰って最初にすることは決まっています。床に寝そべった猫の、あたたかいお腹に手をのせること。ただそれだけで、張りつめていた何かがほどけていきます。

猫は、こちらの都合などお構いなしです。呼んでも来ない。構ってほしいときだけ膝に乗る。最初は身勝手にも思えましたが、今はそのマイペースさに救われています。「がんばらなくていい」と、言葉ではなく態度で教えてくれている気がするのです。

夜、キーボードを打つ手の横で丸くなって眠る姿を見ていると、明日もまあ、なんとかやっていけるかと思えてきます。特別な出来事ではありません。でも、その「なんでもなさ」こそが毎日をそっと支えてくれている——そう感じています。
EOF

cat > "$DIR/2026-06-24_03_辞めたい瞬間.txt" <<'EOF'
【ご提案】
はじめまして。働き方や日常の心情をテーマに、共感ベースの記事を書いております。

「会社を辞めたいと思った瞬間」は、読者が「自分のことだ」と感じる具体的な場面をどれだけリアルに描けるかが勝負だと考えています。説教くさくならず、等身大で。AIで下書き→自分で推敲し、自然で読みやすい文章に仕上げます。短納期にも対応します。下にサンプルを添付します。

──サンプル──
「会社、辞めたいな」とはじめて本気で思った日

きっかけは、たぶん大きな事件ではありませんでした。残業を終えて駅に向かう途中、ふと足が止まって「自分は何のためにここにいるんだろう」と思った。ただそれだけです。

理不尽な一言、報われない頑張り、周りと比べて焦る気持ち。ひとつひとつは耐えられる小ささなのに、積み重なるとボディブローのように効いてくる。あの日の帰り道は、いつもより夜風が冷たく感じました。

今になって思うのは、あの「辞めたい」は、逃げたい気持ちと同じくらい「ちゃんと生きたい」という気持ちの裏返しだったということ。答えはまだ出ていません。でも、立ち止まって考えた時間は、無駄ではなかったと思っています。
EOF

# 検索ページを開く
open "https://crowdworks.jp/public/jobs/search?order=new&search%5Bkeywords%5D=AI%20%E3%83%A9%E3%82%A4%E3%83%86%E3%82%A3%E3%83%B3%E3%82%B0" 2>/dev/null || true
# 応募文3件をTextEditで開く（コピー元）
open -e "$DIR/2026-06-24_01_AI活用記事.txt" 2>/dev/null || true
open -e "$DIR/2026-06-24_02_動物エピソード.txt" 2>/dev/null || true
open -e "$DIR/2026-06-24_03_辞めたい瞬間.txt" 2>/dev/null || true
# 1件目はクリップボードにも入れておく
pbcopy < "$DIR/2026-06-24_01_AI活用記事.txt" 2>/dev/null || true

echo ""
echo "✅ 応募文3件をTextEditで開きました（①はクリップボードにもコピー済み）。"
echo "   各案件をブラウザで開き、対応するウィンドウの文章を ⌘A→⌘C でコピー → メッセージ欄に貼り付け → 応募。"
echo "   ファイル: $DIR/ に保存済み。"
